/** "YYYY-MM-DD" を受け取り曜日を返す（タイムゾーン非依存。0=日 … 6=土） */
function ymdToDow(ymd: string): number {
  const [y, m, d] = ymd.split("-").map(Number);
  return new Date(Date.UTC(y, m - 1, d, 12)).getUTCDay();
}

/** 与えられた日付（YYYY-MM-DD or Date）の YYYY-MM-DD を返す */
function toYmd(date: Date | string): string {
  if (typeof date === "string") return date.slice(0, 10);
  const y = date.getFullYear();
  const m = String(date.getMonth() + 1).padStart(2, "0");
  const d = String(date.getDate()).padStart(2, "0");
  return `${y}-${m}-${d}`;
}

/** 祝日（振替・国民の休日含む）か */
export function isHoliday(date: Date | string): boolean {
  return HOLIDAYS.has(toYmd(date));
}

/** 土曜・日曜・祝日のいずれかか（=「土日祝」料金区分） */
export function isWeekendOrHoliday(date: Date | string): boolean {
  const ymd = toYmd(date);
  const dow = ymdToDow(ymd);
  return dow === 0 || dow === 6 || HOLIDAYS.has(ymd);
}
